	<h2 class="pb-10 text-4xl/9 md:text-4xl/15 font-semibold">Founding of W9YT</h2>

	<p class="py-4 font-normal italic">
		Published in The Badger State Smoke Signals - April 1983 issue
		<br />
		Originally titled "The Badger Amateur Radio Society Today"
		<br>
		by Steve Dubberstein, NA9D/VS6WO (ex. WB9PYE)
	</p>

	<p class="py-4">
		Over 25 years have passed since the second coming of W9YT. Hundreds of students have operated
		the station in this time, W9YT providing a welcome relief from the everyday chores of college
		life.
	</p>
	<p class="py-4">
		W9YT is located in room B314 in the basement of the UW Engineering building. The station
		consists of a Kenwood TS-830S and a tough homebrew kilowatt feeding monobanders on 20, 15, and
		10; with directional dipoles on 30, 40, 80, and 160 meters. The station is also active on 2 and
		6 meters, and just last semester finished up WAS on 6.
	</p>
	<p class="py-4">
		W9YT was probably best known nationwide in the late 60’s and early 70’s when a Drake B line was
		combined with the antennas and Kilowatt to make it one of the finest club stations in the
		country. With the help of some very ambitious and gifted operators, W9YT has placed as high as
		1st in the nation in CW SS, 2nd in phone SS, and 6th in CQWW DX multi-multi competition. The
		club has also made BPL several times, and is still active today on the traffic nets.
	</p>
	<p class="py-4">
		The Badger Amateur Radio Society today consists of about 30 student members. Daily activity
		includes skeds, casual operating, and traffic handing. W9YT can be heard in most major contests
		and field day. One of the more popular activities is an occasional on-foot transmitter hunt,
		with fox hiding somewhere in the campus area.
	</p>
	<p class="py-4">
		Although the University provides a small room for a shack and a room for antennas, BARS receives
		no funding and is thus supported mainly by student member’s dues. The station is plagued by RFI
		problems, and must maintain a low profile in order not to become extinct (as has recently
		happened to W9YM at the University of Illinois). We invite alumni and non-alumni alike to drop
		in for an eyeball anytime, or just give us a shout on your way though Madison on W9YT/R (146.685
		out, 146.085 in). W9YT is still today regarded as one of the finest college clubs in America,
		and hopes to be able to provide an outlet for all future hams that attend the UW.
	</p>

