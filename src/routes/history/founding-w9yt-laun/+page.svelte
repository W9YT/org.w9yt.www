<div class="mx-auto max-w-3xl px-5 pt-10 text-lg font-light">
	<h2 class="pb-10 text-4xl/15 font-semibold">Founding of W9YT</h2>

	<p class="py-4 font-normal italic">
		Published in The Badger State Smoke Signals - January, February, and March 1983 issue
		<br />
		by Fred Laun, HK3NBB/K3ZO
	</p>

	<p class="py-4">
		This scribe was in on the founding of the Badger Amateur Radio Society at the University of
		Wisconsin. At the time, i was W9SZR, originally from Kiel, Wisconsin. The other co-founders were
		W9VAK of Fond du Lac, now in the Chicago area; W9SDC, then and now of the Chicago area; W9VOO,
		now W9OO of the Chicago area; W9RQN, of Superior, now K6EUW of San Diego; and W9ZQA from Chetek,
		now KE6MD of Berkeley, California.
	</p>
	<p class="py-4">
		At the time I arrived on the UW campus in the fall of 1955, there was only one place on the
		campus to operate, and that was at W9GOC, a dormitory club station at lakeside (Lake Mendota)
		with an old Meisner Signal Shifter driving a single 813 amp, which had been built without
		neutralization. The only band on which the monster would operate without problems was 40 meters.
	</p>
	<p class="py-4">
		There was an HQ-129X Hammarlund, which, for its day, was a pretty good receiver. And, the
		antenna was an end-fed Zepp, which was strung between the tops of two dormitories and worked
		quite well.
	</p>
	<p class="py-4">
		In those days, the FCC meted out hellfire and brimstone to those who dared to operate a club
		station using their own callsigns, but occasionally it was difficult to resist temptation. Thus
		it was that (after a brand new Heath DX-100 had been acquired and assembled) one day W9ZQA heard
		CR8AC (Goa, a Portuguese colony then in India) coming through on 20 cw and gave him a call. Lo
		and behold, CR8AC came back. The wise old DX sages of Madison, at the next meeting of the Four
		Lakes Amateur Radio Club – which most of us were members of – pronounced the CR8AC a bootlegger.
		But eventually ZQA got the QSL, which he cherishes to this day.
	</p>
	<p class="py-4">
		When not in use as a ham rig, the DX-100 could be found on 1610 KHz (that was the lowest we
		could get the vfo to by detuning all the slugs) masquerading as “WCCC, Clark City, Missouri.” (I
		hope the statute of limitations has run out.)
	</p>
	<p class="py-4">
		This station had some of the most hilarious ads ever heard in broadcasting up to that time,
		perhaps only exceeded in later times when K9ELT (now N6ZZ) was working as a BC engineer at WIBA.
		We tested the field strength of the station by driving all around Lake Mendota, and were happy
		when it could be heard in most places.
	</p>
	<p class="py-4">
		Since VOO and ZQA were working in broadcasting at the time, the tapes were professionally done.
		A fellow named Paul Carlson, who never became a ham, also made important contributions to the
		program. Transmissions were limited to about two hours per broadcast, not because we were
		worried about the FCC, but because it took about 6 hours of work to get a decent hour of
		programming taped.
	</p>
	<p class="py-4">
		Our psychic rewards were reaped the following morning, when silently accompanying anonymous
		groups of students on their way to classes, we heard about “that crazy radio station from
		Missouri.”
	</p>
	<p class="py-4">
		While transmissions from “WCCC” were intended to be heard by students, those of W9GOC were not.
		Yet, given the almost non-existent image rejection characteristic of the cheap “AC-DC” sets of
		the day – and every student had one – W9GOC was frequently heard and cussed at from one end of
		dormitory row to the other.
	</p>
	<p class="py-4">
		Through some perversity of Mother Nature, it turned out that when someone checked into the
		Badger Emergency Net, the image fell right on top of WIBA. In those days, it should be added,
		thirty-nine fifty was not merely a net frequency; it was also known as the international testing
		frequency, scene of the great battles of the Kilowatts between W9HIF and W9PYM; of the great
		humorous monologues and flights of fancy between W9HIF and W9LUQ.
	</p>
	<p class="py-4">
		It was the home of the bootlegger and frustrated would-be-hams from rural Janesville who would
		play music on request from a fabulous record library. Throughout the daylight hours, in other
		words, you had to participate on thirty-nine fifty or be deemed to ignorance about what was
		happening in Wisconsin Ham Radio.
	</p>
	<p class="py-4">
		Eventually, tired of escaping through a back window of the clubroom to get away from angry
		students wielding baseball bats, we decided to do something else to establish a club station
		somewhere else.
	</p>
	<p class="py-4">
		W9VAK was on some kind of a student engineering council which had $350 to burn. In the
		electrical engineering building was the MARS station of Army ROTC, K9WBJ. (Yes folks, in those
		days all stations with the prefix K9 were military club stations; only in the 2nd and 6th
		districts had the FCC run out of W prefixes) The Army had never been able to get permission to
		put up more than a dipole on the roof, and wanted to add a beam.
	</p>
	<p class="py-4">
		Thanks to our faculty adviser, Prof. Glenn Keehler, we had the permission for the beam.
		Therefore we talked the Army into letting us use the station in off-hours in return for letting
		them use the beam during classroom hours – the $450 went to buy a World Radio TH-4, one of the
		first tribanders (the antenna division of World Radio later branched off on its own as Hy-Gain),
		and rotator, plus coax and rotor cable. There was already a crankover tower on the roof.
	</p>
	<p class="py-4">
		The Badger Amateur Radio Society was thus established., affiliated with the EE Faculty. Prof.
		Keehler, rummaging thru old files came up with a license issued to the U.W. Physics Department
		for an Amateur club station in the thirties with the call W9YT. On presentation of that license
		to the FCC, they agreed to re-issue the call to our new society.
	</p>
	<p class="py-4">
		In those days, two-letter calls had not yet been reissued and it was rare to hear one on the
		air. The idea that we young, wet-behind-the-ears types were coming on with a two-letter call
		caused no end of criticism on the Badger Net the first few times we checked in. At one time, it
		had been a tradition to reserve the “Y” following the number for college clubs, i.e.
		W9YH/Illinois, W9YB/Purdue,&nbsp;<a class="underline" href="http://w6yx.stanford.edu/w6yx.html">W6YX, Stanford</a
		>&nbsp;, etc.
	</p>
	<p class="py-4">
		For those of us who had come from all over the state to attend the U.W., the Army equipment we
		got permission to use was like a dream station. At that time it was rather rare to hear a
		kilowatt on the air. You could either brew your own or buy a Collins KW-1 or a Viking KW,
		spending kilobucks in the days when a kilobuck was a kilobuck. We had a BC-610 which could run
		about 700 watts input, quite a lot of soup. Plus 2 Collins R-388-URR receivers, Amateur RTTY,
		etc. W9ZQA drew the lot to make the first contact from W9YT in August, 1957, and made the first
		trembling CQ to be answered by W9TPS who lived almost across the street on 20 c.w.
	</p>
	<p class="py-4">
		The first log of W9YT was somehow lost along the way, but it is my understanding that every log
		from the second onward is still available for current members to peruse.
	</p>
	<p class="py-4">
		It would indeed have been a shame had the FCC eliminated club station licenses. With the old
		logs in hand, a current member can get the feeling that he is a part of a tradition, forming
		part of a chain between past and future members. For example, in the W9YT log will be found a
		contact with JT1AA, the first station ever on the air from Mongolia.
	</p>
	<p class="py-4">
		This was a contact made by yours truly as a direct result of a challenge from W9LPL to all the
		members. The first operator to work JT1AA from W9YT would be awarded a free beer from one of the
		campus sudseries.
	</p>
	<p class="py-4">
		It is still a thrill for me every time I hear W9YT on the air, as it immediately triggers
		memories of some of the most pleasant experiences of my life. Some pretty well-known ops came
		out of W9YT: There was K9ELT from Madison, now N6ZZ, who has operated all over the Caribbean,
		and put HH9DL on the air when HH was still very, very rare. There was K9ZMS from Sun Prairie,
		now N6NA, who has operated from Clipperton. W9VZL from Milwaukee is now N4XX, CQ’s Washington
		Columnist.
	</p>
	<p class="py-4">Yes, indeed, any operator at W9YT today is part of a long and proud tradition!</p>
