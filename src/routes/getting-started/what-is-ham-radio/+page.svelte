	<h3 class="text-xl/7 md:text-xl/12 font-semibold">
		Why amateur radio, instead of, say, CB or walkie-talkies?
	</h3>
	<p class="py-4">
		Radio spectrum is divvied up into different "services", including amateur radio. 
		The amateur radio service requires licensing, but at the same time, gives the widest spread of spectrum a private user can use, with the very unique privilege of being able to legally design and use your own antennas/radios/ancillary equipment on the air! 
	</p>


	<h3 class="text-xl/7 md:text-xl/12 font-semibold">
