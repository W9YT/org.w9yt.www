	<p class="py-4">
		Amateur Radio (ham radio) is a popular hobby and service that brings people, electronics and communication together. 
		People use ham radio to talk across town, around the world, or even into space, all without the Internet or cell phones. 
		It's fun, social, educational, and can be a lifeline during times of need. 
	</p>

	<h3 class="text-xl/7 md:text-xl/12 font-semibold">
		So... why?
	</h3>
	<p class="py-4">
		Well... it's cool to build an antenna and talk across the globe. 
		Some people enjoy the challenge of simply reaching far away and making contacts, others enjoy meeting people on the air and actually carrying on conversations. 
	</p>
	<p class="py-4">
		There's also an aspect of building and experimenting with electronics & digital communications. 
		Others compete in "DX contests," where the object is to see how many hams in distant locations they can contact. 
	</p>
	<p class="py-4">
		Contacts can be as simple as exchanging who you are (with callsigns, in our case, W9YT), where you are, and whether you can actually hear the schmuck on the other end. 
		These contacts were confirmed by sending QSL cards (postcards) back and forth, which could be kept for bragging rights! 
		This has led to a rich award and contest culture which still exists in amateur radio today. 
	</p>
